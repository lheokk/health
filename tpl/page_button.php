<div class="row">
        <div class="col">
            <div class="index-text"><p>Бланк установленного образца личной медицинской книжки выдает специализированное медицинское учреждение, свободной продаже они не подлежат, поскольку являются бланками строгой отчетности. Оформлением личной медицинской книжки имеют право заниматься исключительно центры гигиены и эпидемиологии, имеющие соответсвующие разрешения и сертификацию, согласно Приказа N 402. Все без исключения документы подлежат учету, и вносятся в единый реестр личных медкнижек. </p>
