        </div>
    </div>

    <div class="row">
        <div class="col">
            <div class="ul_class ">   
                <ul> 
                    <li class="s">Приказом Роспотребнадзора от 25.05.2005 №402 «О личной медициснкой книжке и санитарном паспорте»</li>
                    <li class="s">Федеральный закон от 30 марта 1999г. N 52-Ф3 «О санитарно-эпидемиологическом благополучии населения»</li>
                    <li class="s">Приказом Минздрава РФ от 29.06.2000 N 229 «О профессиональной гигиенической подготовке и аттестации должностных лиц и работников организаций» (вместе с «Инструкцией о порядке проведения профессиональной гигиенической подготовки и аттестации должностных лиц и работников организаций, деятельность которых связана с производстовом, хранением, транспортировкой и реализацией пищевых продуктов и питьевой воды, воспитанием и обучением детей, коммунальным и бытовым обслуживанием населения») (Зарегистрировано в Минюсте РФ 20.07.2000 N 2321)</li>
                    <li class="s">Письмом Минздрава РФ то 07.08.2000 N 1100/2196-0-117 «О направлении перечная профессий». </li>
                    <li class="s">Приказом Мин-соцразвития от 12.04.2011 N 302н (в части перечная лиц, обязанных проходить обязательные первичные и переодические медицинские осмотры).</li>
