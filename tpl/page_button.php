        <div class="col">
            <div class="index-text"><p>Действующее законодательство прямо не регулирует вопрос оплаты оформления документа. Существует практика, согласно которой работодатель может компенсировать трудящемуся стоимость такого бланка, включив его в расходы на проведение предварительного медицинского осмотра при приеме на работу, поскольку оформление медкнижки можно считать неотъемлемой частью предварительного осмотра.  </p>
